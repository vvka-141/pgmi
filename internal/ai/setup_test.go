func TestParseManaged_RoundTrip(t *testing.T) {
	f := generate(t)
	m := ParseManaged(f.Content)

	if !m.HasStamp {
		t.Fatal("expected HasStamp")
	}
	if m.Edited() {
		t.Error("freshly generated file should not be reported as edited")
	}
	if m.Stamp.Version != "1.2.3" {
		t.Errorf("Stamp.Version = %q, want 1.2.3", m.Stamp.Version)
	}
	if m.Stamp.Source != ModulePath {
		t.Errorf("Stamp.Source = %q, want %q", m.Stamp.Source, ModulePath)
	}
}

func TestParseManaged_DetectsEditsAnywhere(t *testing.T) {
	f := generate(t)

	cases := map[string]string{
		"body edit":     strings.Replace(f.Content, "# pgmi", "# pgmi EDITED", 1),
		"trailing edit": f.Content + "\nappended\n",
		"no stamp":      "just some prose with no stamp\n",
	}
	for name, content := range cases {
		t.Run(name, func(t *testing.T) {
			if !ParseManaged(content).Edited() {
				t.Errorf("%s should be reported as edited", name)
			}
		})
	}
}

func TestParseManaged_StaleNotEdited(t *testing.T) {
	// A file generated by an older build: different body, but its own stamp is
	// internally consistent. It must read as not-edited (pristine) but its body
	// must differ from the current generation, so `check` flags it stale.
	old := RenderManaged("# pgmi\n\nold core body\n", Stamp{Version: "0.0.1", Source: ModulePath})
	m := ParseManaged(old)

	if m.Edited() {
		t.Error("a pristine older-version file must not read as edited")
	}
	current := generate(t)
	if m.Body == ParseManaged(current.Content).Body {
		t.Error("expected old body to differ from current generation")
	}
}

