package ai

import (
	"strings"
	"testing"
)

func generate(t *testing.T) PlannedFile {
	t.Helper()
	files, err := GenerateSetup("claude", Stamp{Version: "1.2.3", Source: ModulePath})
	if err != nil {
		t.Fatalf("GenerateSetup: %v", err)
	}
	if len(files) == 0 {
		t.Fatal("expected at least 1 file")
	}
	return files[0]
}

func TestGenerateSetup_ClaudeSkill(t *testing.T) {
	f := generate(t)

	if f.RelPath != "pgmi/SKILL.md" {
		t.Errorf("RelPath = %q, want pgmi/SKILL.md", f.RelPath)
	}

	for _, want := range []string{
		"name: pgmi",
		"description:",
		"deploy.sql",
		"pgmi_plan_view",
		"CALL pgmi_test()",
		ModulePath,
	} {
		if !strings.Contains(f.Content, want) {
			t.Errorf("generated skill missing %q", want)
		}
	}

	if strings.Contains(f.Content, coreMarker) {
		t.Errorf("core marker %q was not substituted", coreMarker)
	}
}

